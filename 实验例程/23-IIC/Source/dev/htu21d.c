/*********************************************************************************************
* 名称:htu21d_init()
* 功能:htu21d初始化
* 参数:无
* 返回:无
* 修改:
* 注释:
*********************************************************************************************/
void htu21d_init(void)
{
  char cmd = 0xfe;
  HTU21DGPIOInit();
  i2c_write(HTU21D_ADDR, &cmd, 1); 								//reset
  delay_ms(20);
}

/*********************************************************************************************
* 名称:htu21d_read_temp()
* 功能:读取温度原始数据
* 参数:无
* 返回:char dat -- 未处理的温度值
* 修改:
* 注释:
*********************************************************************************************/
char htu21d_read_temp(void)
{
  char cmd = 0xf3;
  char dat;
  
  i2c_write(HTU21D_ADDR, &cmd, 1);
  delay_ms(50);
  i2c_read(HTU21D_ADDR, &dat, 1);
  return dat;
}

