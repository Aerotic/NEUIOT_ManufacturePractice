/*********************************************************************************************
* 名称:htu21d_t()
* 功能:读取温度
* 参数:无
* 返回:-1/float t -- 处理后的温度值
* 修改:
* 注释:
*********************************************************************************************/
float htu21d_t(void)
{
  char cmd = 0xf3;
  char dat[4];
  i2c_write(HTU21D_ADDR, &cmd, 1);
  delay_ms(50);
  if (i2c_read(HTU21D_ADDR, dat, 2) == 2) {
    if ((dat[1]&0x02) == 0) {
      float t = -46.85f + 175.72f * ((dat[0]<<8 | dat[1])&0xfffc) / (1<<16);
      return t;
    }
  }
  return -1;
}

/*********************************************************************************************
* 名称:htu21d_h()
* 功能:读取湿度
* 参数:无
* 返回:-1/float h -- 处理后的湿度值
* 修改:
* 注释:
*********************************************************************************************/
float htu21d_h(void)
{
  char cmd = 0xf5;
  char dat[4];
  
  i2c_write(HTU21D_ADDR, &cmd, 1);
  delay_ms(50);
  if (i2c_read(HTU21D_ADDR, dat, 2) == 2) {
    if ((dat[1]&0x02) == 0x02) {
      float h = -6 + 125 * ((dat[0]<<8 | dat[1])&0xfffc) / (1<<16);
      return h;
    }
  }
  return -1;
}


